/******************************************************************************
	関数名		：Aud_incr
	引数		：BYTE cntr		:インクリメントするカウンタ
				：BYTE limit	:カウンタのリミット
	戻り値		：無し
	機能概略	：カウンタインクリメント処理
------------------------------------------------------------------------------
	修正履歴
	 番号		日付		担当者		修正内容
	【000】		06/04/05	ISHIHARA	新規作成
******************************************************************************/
BYTE Aud_incr( BYTE cntr, BYTE limit )
{
	if ( ++cntr > limit )
	{
		cntr = limit;
	}
	return( cntr );
}

/******************************************************************************
	関数名		：Aud_decr
	引数		：BYTE cntr		:デクリメントするカウンタ
				：BYTE limit	:カウンタのリミット
	戻り値		：無し
	機能概略	：カウンタデクリメント処理
------------------------------------------------------------------------------
	修正履歴
	 番号		日付		担当者		修正内容
	【000】		06/04/07	ISHIHARA	新規作成
******************************************************************************/
BYTE Aud_decr( BYTE cntr, BYTE limit )
{
	if ( cntr > limit )
	{
		cntr--;
	}
	return( cntr );
}

/******************************************************************************
	関数名		：Aud_add
	引数		：BYTE src		:加算される値
				：BYTE des		:加算する値
				：BYTE limit	:リミット
	戻り値		：無し
	機能概略	：任意の値を加算する処理（加算結果が255以下のみ対象）
------------------------------------------------------------------------------
	修正履歴
	 番号		日付		担当者		修正内容
	【000】		06/04/05	ISHIHARA	新規作成
******************************************************************************/
BYTE Aud_add( BYTE src, BYTE des, BYTE limit )
{
	if ( (src += des) > limit )
	{
		src = limit;
	}
	return( src );
}

/******************************************************************************
	関数名		：Aud_sub
	引数		：BYTE src		:被減算値
				：BYTE des		:減算値
	戻り値		：無し
	機能概略	：任意の値を減算する処理
				：（減算値が被減算値より大きい場合、結果は"0"になる）
------------------------------------------------------------------------------
	修正履歴
	 番号		日付		担当者		修正内容
	【000】		06/04/05	ISHIHARA	新規作成
******************************************************************************/
BYTE Aud_sub( BYTE src, BYTE des )
{
	if ( src <= des )
	{
		src =0;
	}
	else
	{
		src -= des;
	}
	return( src );
}

